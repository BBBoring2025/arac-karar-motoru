/**
 * Belirtilen dugumden cikan (veya cift yonlu ise gelen) tum kenarlari dondurur.
 */
export function getEdgesFrom(nodeId: string): RouteEdge[] {
  return routeEdges.filter(
    (e) => e.from === nodeId || (e.bidirectional && e.to === nodeId)
  );
}

/**
 * Iki dugum arasindaki kenari dondurur (varsa).
 */
export function getEdgeBetween(
  fromId: string,
  toId: string
): RouteEdge | undefined {
  return routeEdges.find(
    (e) =>
      (e.from === fromId && e.to === toId) ||
      (e.bidirectional && e.from === toId && e.to === fromId)
  );
}
